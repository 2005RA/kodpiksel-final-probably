// scripts/sync-task-rewards.mjs
//
// Generates the SQL that keeps Supabase's `task_rewards` registry in sync
// with the actual lesson/challenge/level-up content in src/data. This is
// the one script to run whenever you add or change a lesson, a challenge,
// or a level tier — you edit the numbers in ONE place (the data file you'd
// edit anyway), run this, and apply the SQL it prints. No more hand-typing
// the same chip/key/hourglass amounts into a second file.
//
// Usage:
//   npm run sync-rewards
//   → writes supabase/generated-task-rewards.sql
//   → paste that file into the Supabase SQL Editor and run it
//     (or hand it to Claude and ask it to apply the migration)
//
// Adding a new course: add one entry to the COURSES array below with its
// courseId and the import paths for its lesson/challenge data. That's the
// only code change this script ever needs — everything else it reads
// straight from your existing data files.

import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '..');

// ── Register each course's data source here ──────────────────────────────
// lessons: module exporting { [courseId]_LESSON_CHIPS-equivalent } — really
// just needs a `LESSON_CHIPS` map of { [lessonId]: chips }.
// challenges: module exporting an id-keyed object with
// { chips, keys, hourglasses } per entry.
// Missing a course's file (e.g. python has no content yet) is fine — it's
// just skipped, nothing errors.
const COURSES = [
  {
    courseId: 'htmlcss',
    lessonsPath: '../src/data/lessons/htmlcss.js',
    challengesPath: '../src/data/challenges/htmlcss.js',
    challengesExportName: 'HTMLCSS_CHALLENGES',
  },
  {
    courseId: 'python',
    lessonsPath: '../src/data/lessons/python.js',
    challengesPath: '../src/data/challenges/python.js',
    challengesExportName: 'PYTHON_CHALLENGES',
  },
];

const rows = []; // { task_id, chips, keys, hourglasses }

function addRow(task_id, chips = 0, keys = 0, hourglasses = 0) {
  rows.push({ task_id, chips, keys, hourglasses });
}

for (const course of COURSES) {
  // ── Lessons: chips only ──
  try {
    const mod = await import(course.lessonsPath);
    const chipsMap = mod.LESSON_CHIPS;
    if (chipsMap) {
      for (const [lessonId, chips] of Object.entries(chipsMap)) {
        addRow(`${course.courseId}-lesson-${lessonId}`, chips, 0, 0);
      }
      console.log(`✓ ${course.courseId}: ${Object.keys(chipsMap).length} lessons`);
    }
  } catch {
    console.log(`– ${course.courseId}: no lesson data file yet, skipping`);
  }

  // ── Challenges: chips row + keys/hourglasses "-nonchip" row ──
  try {
    const mod = await import(course.challengesPath);
    const challenges = mod[course.challengesExportName];
    if (challenges) {
      for (const [id, c] of Object.entries(challenges)) {
        const taskId = `${course.courseId}-challenge-${id}`;
        addRow(taskId, c.chips || 0, 0, 0);
        addRow(`${taskId}-nonchip`, 0, c.keys || 0, c.hourglasses || 0);
      }
      console.log(`✓ ${course.courseId}: ${Object.keys(challenges).length} challenges`);
    }
  } catch {
    console.log(`– ${course.courseId}: no challenge data file yet, skipping`);
  }
}

// ── Level-ups: shared across all courses (level is global, not per-course) ──
try {
  const mod = await import('../src/data/levelRewards.js');
  const levels = mod.LEVEL_REWARDS;
  for (const [level, cfg] of Object.entries(levels)) {
    addRow(`levelup-${level}`, cfg.chips || 0, cfg.keys || 0, cfg.hourglasses || 0);
  }
  console.log(`✓ level-ups: ${Object.keys(levels).length} tiers`);
} catch (e) {
  console.error('Could not read levelRewards.js:', e.message);
  process.exit(1);
}

// ── Generate SQL ──
const values = rows
  .map(r => `  ('${r.task_id}', ${r.chips}, ${r.keys}, ${r.hourglasses})`)
  .join(',\n');

const sql = `-- AUTO-GENERATED by scripts/sync-task-rewards.mjs — do not hand-edit.
-- Regenerate with \`npm run sync-rewards\` whenever lesson/challenge/level
-- content changes, then apply this file in the Supabase SQL Editor.
insert into public.task_rewards (task_id, chips, keys, hourglasses) values
${values}
on conflict (task_id) do update set
  chips       = excluded.chips,
  keys        = excluded.keys,
  hourglasses = excluded.hourglasses;

-- Remove any registry rows for task ids that no longer exist in the data
-- files (e.g. a lesson/challenge/level you deleted). Safe to run — this
-- only affects the registry, not anyone's already-earned rewards.
delete from public.task_rewards
where task_id not in (${rows.map(r => `'${r.task_id}'`).join(', ')});
`;

const outPath = join(repoRoot, 'supabase', 'generated-task-rewards.sql');
writeFileSync(outPath, sql);
console.log(`\n${rows.length} task_rewards rows written to supabase/generated-task-rewards.sql`);
console.log('Next: paste that file into the Supabase SQL Editor and run it (or ask Claude to apply it).');